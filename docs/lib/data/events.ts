export interface Event {
  id: string;
  name: string;
  /** ISO date (yyyy-mm-dd), formatted for display client-side. */
  date: string;
  /** Venue name, or `null` while unassigned — publishing requires one. */
  venue: string | null;
  category: string;
  /** Ticket price in EUR. `0` marks a free event. */
  price: number;
  status: EventStatus;
  /** Active ticket reservations — the blast radius named when deleting. */
  reservations: number;
}

